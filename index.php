    $(".contentThree").hide();
    $(function(){
        $(".textHolder").delay(2000).animate({
            left: '70vw',
            opacity: '0',
            height: '60px',
            width: '300px',
            marginLeft : '50vw'
        }, 3000, function(){
            $(".secondTextHolder").show().animate({
                left: '0vw',
                opacity: '0',
                height: '60px',
                width: '300px',
                marginLeft : '1vw'
            }, 3000, function(){
            $(".textHolderC").show().animate({
                    left: '0vw',
                    opacity: '100',
                    height: '60px',
                    width: '300px',
                    marginLeft : '1vw'
                }, 3000)
            });
        });
    });

    $(".contentText").mouseenter(function(){
        $(".initText").animate({
            left: '70vw',
            opacity: '0',
            height: '60px',
            width: '500px',
            marginLeft : '50vw'
        }, 3000, function(){
            $(".initText").hide();
            $(".secondText").show().animate({
                
            left: '0vw',
            opacity: '100',
            height: '60px',
            width: '400px',
            marginLeft : '5vw'
            }, 3000)
        });
    });
    $('#carouselDevice').on('slid.bs.carousel', function () {
        var src = $('.active').find('h1').attr('data-name');
       if(src == "textInit"){
            $(".contentOne").show();
            $(".contentTwo").hide();
            $(".contentThree").hide();
       }else if(src == "textSec"){
            $(".contentOne").hide();
            $(".contentTwo").show();
            $(".contentThree").hide();
       }else if(src == "textThird"){
             $(".contentOne").hide();
            $(".contentTwo").hide();
            $(".contentThree").show();
       }
    })    
});
</script>